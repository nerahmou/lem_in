#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			j = i;
		i++;
	}
	if (s[i] == '\0' && c == 0)
		return ((char *)s + i);
	else if (j > 0 || (j == 0 && s[j] == c))
		return ((char *)s + j);
	return (NULL);
}
