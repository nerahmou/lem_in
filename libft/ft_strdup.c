#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*dup;

	dup = ft_strnew(ft_strlen(src));
	if (!dup)
		return (NULL);
	i = -1;
	while (src[++i])
		dup[i] = src[i];
	dup[i] = 0;
	return (dup);
}
