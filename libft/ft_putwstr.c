#include "libft.h"

int		ft_putwstr(wchar_t *str)
{
	int i;
	int nb;

	i = 0;
	nb = 0;
	while (str[i])
	{
		nb += ft_putwchar(str[i]);
		i++;
	}
	return (nb);
}
