#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	unsigned long i;

	i = 0;
	while (s[i] != c && s[i] != '\0')
		i++;
	if (s[i] == c)
		return ((char *)&s[i]);
	return (NULL);
}
