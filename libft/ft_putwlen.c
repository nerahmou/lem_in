#include "libft.h"

int		ft_putwlen(wchar_t c)
{
	int i;

	i = 0;
	if (c <= 0x7F)
		i++;
	else if (c <= 0x7FF)
		i = 2;
	else if (c <= 0xFFFF)
		i = 3;
	else if (c <= 0x10FFFF)
		i = 4;
	return (i);
}
